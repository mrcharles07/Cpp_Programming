#include<iostream>
using namespace std;

int main()
{
    int a;
    cout <<"size of int(a):"<< sizeof(a)<<" byte"<<endl;

    char b;
    cout <<"size of char(b):"<< sizeof(b)<<" byte"<<endl;

    float c;
    cout <<"size of float(c):"<< sizeof(c)<<" byte"<<endl;

    double d;
    cout <<"size of double(d):"<< sizeof(d)<<" byte"<<endl;

    short e;
    cout <<"size of short(e):"<< sizeof(e)<<" byte"<<endl;

    return 0;
}